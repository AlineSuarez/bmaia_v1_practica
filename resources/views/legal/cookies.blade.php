    <div class="section">
        <h2 id="que-son">1. ¿Qué son las cookies?</h2>
        <p>
            Las cookies son pequeños archivos de texto que se almacenan en tu dispositivo (computador, notebook, tablet,
            o teléfono móvil) cuando visitas un sitio web. Permiten que el sitio web recuerde tus acciones y preferencias,
            como el idioma, el tamaño de la letra y otras preferencias de visualización durante un período de tiempo, para
            que no tengas que volver a configurarlas cada vez que regresas al sitio o navegas de una página a otra.
        </p>
    </div>

    <div class="section">
        <h2 id="tipos">2. Tipos de cookies que utilizamos</h2>

        <h3>Cookies propias</h3>
        <p>
            Son cookies establecidas por nosotros (Bee Fractal SpA) y se utilizan para el funcionamiento básico del Sitio
            Web.
        </p>
        <ul>
            <li><strong>PHPSESSID:</strong> Cookie de sesión que permite mantener la sesión del usuario activa mientras
                navega por el sitio (Duración: Sesión).</li>
            <li><strong>cookie_consent:</strong> Cookie persistente que registra si el usuario ha aceptado o rechazado el
                uso de cookies (Duración: 1 año).</li>
        </ul>

        <h3>Cookies de terceros</h3>
        <p>
            Son cookies establecidas por otros sitios web o servicios que utilizamos en nuestro Sitio Web.
        </p>
        <ul>
            <li>
                <strong>Google Analytics:</strong> Cookies analíticas que recopilan información anónima sobre cómo los
                usuarios
                utilizan el Sitio Web, como las páginas que visitan, el tiempo que pasan en cada página, entre otros
                aspectos.
                Utilizamos esta información para mejorar el Sitio Web.<br>
                Proveedor: Google, Política de privacidad: <a href="https://policies.google.com/privacy?hl=es"
                    target="_blank">https://policies.google.com/privacy?hl=es</a>,
                Duración: _ga: 2 años, _gid: 24 horas.
            </li>
            <li><strong>Plataforma de publicidad:</strong> Cookies de publicidad que se utilizan para mostrar anuncios
                relevantes para el usuario.</li>
            <li><strong>Red social:</strong> Cookies de redes sociales que permiten compartir contenido del Sitio Web en
                redes sociales.</li>
        </ul>
    </div>

    <div class="section">
        <h2 id="otras-tecnologias">3. Otras tecnologías de seguimiento</h2>
        <p>
            Además de las cookies, también podemos utilizar otras tecnologías de seguimiento similares, como:
        </p>
        <ul>
